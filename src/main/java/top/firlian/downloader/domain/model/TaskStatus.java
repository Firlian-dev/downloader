package top.firlian.downloader.domain.model;

/**
 * Перечисление статусов задачи загрузки.
 * Отражает текущее состояние процесса загрузки медиа файла.
 */
public enum TaskStatus {
    /** Задача ожидает выполнения */
    PENDING,
    /** Задача в процессе загрузки */
    DOWNLOADING,
    /** Задача успешно завершена */
    COMPLETED,
    /** Задача завершена с ошибкой */
    FAILED
}
