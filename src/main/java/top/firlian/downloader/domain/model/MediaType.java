package top.firlian.downloader.domain.model;

/**
 * Перечисление типов медиа контента.
 * Определяет возможные типы медиа файлов, которые могут быть загружены.
 */
public enum MediaType {
    /** Видео файл */
    VIDEO,
    /** Фото или изображение */
    PHOTO,
    /** Документ или другой файл */
    DOCUMENT,
    /** Аудио файл */
    AUDIO
}
